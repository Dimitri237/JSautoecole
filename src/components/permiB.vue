        <section id="regles">
            <h2>Règles de Circulation</h2>
            <p>Les règles de circulation sont essentielles pour garantir la sécurité de tous les usagers de la route. Voici quelques règles importantes :</p>
            <ul>
                <li><strong>Limitations de vitesse :</strong> Respectez les limitations de vitesse affichées. En agglomération, la vitesse maximale est généralement de 50 km/h.</li>
                <li><strong>Feux de signalisation :</strong> Obéissez aux feux tricol ores. Un feu rouge signifie arrêt, un feu vert signifie que vous pouvez passer, et un feu orange indique qu'il faut s'arrêter si cela est possible.</li>
                <li><strong>Clignotants :</strong> Utilisez vos clignotants pour signaler vos intentions de changement de direction ou de voie.</li>
                <li><strong>Céder le passage :</strong> Céder le passage aux véhicules venant de droite à une intersection sans signalisation.</li>
            </ul>
        </section>

        <section id="securite">
            <h2>Sécurité Routière</h2>
            <p>La sécurité routière est primordiale pour éviter les accidents. Voici quelques conseils :</p>
            <ol>
                <li><strong>Ceinture de sécurité :</strong> Attachez toujours votre ceinture de sécurité, ainsi que celle de vos passagers.</li>
                <li><strong>Alcool et drogues :</strong> Ne conduisez jamais sous l'influence de l'alcool ou de drogues. Cela altère vos réflexes et votre jugement.</li>
                <li><strong>Vigilance :</strong> Restez attentif à votre environnement. Évitez les distractions comme l'utilisation de votre téléphone portable.</li>
                <li><strong>Distance de sécurité :</strong> Maintenez une distance de sécurité suffisante avec le véhicule qui vous précède pour réagir en cas de freinage brusque.</li>
            </ol>
        </section>

        <section id="panneaux">
            <h2>Panneaux de Signalisation</h2>
            <p>Les panneaux de signalisation informent les conducteurs des règles à suivre. Voici quelques types de panneaux :</p>
            <ul>
                <li><strong>Panneaux d'interdiction :</strong> Indiquent ce qui est interdit, comme le panneau "Interdiction de tourner à gauche".</li>
                <li><strong>Panneaux d'obligation :</strong> Indiquent ce que vous devez faire, comme le panneau "Obligation de tourner à droite".</li>
                <li><strong>Panneaux d'avertissement :</strong> Avertissent des dangers potentiels, comme le panneau "Virage dangereux".</li>
                <li><strong>Panneaux d'indication :</strong> Fournissent des informations utiles, comme les panneaux de direction ou les panneaux de distance.</li>
            </ul>
        </section>

