            <h2>Éléments du Véhicule</h2>
            <p>Connaître les différents éléments de votre véhicule est crucial pour une conduite sécurisée. Voici les principaux éléments :</p>
            <ul>
                <li><strong>Volant :</strong> Permet de diriger le véhicule. Il est important de le tenir fermement et de garder les deux mains sur le volant.</li>
                <li><strong>Frein :</strong> Utilisé pour ralentir ou arrêter le véhicule. Il existe généralement deux types de freins : le frein de service (pédale) et le frein à main (levier).</li>
                <li><strong>Accélérateur :</strong> Pédale qui permet d'augmenter la vitesse du véhicule. Appuyez doucement pour éviter une accélération brusque.</li>
                <li><strong>Tableau de bord :</strong> Affiche des informations essentielles comme la vitesse, le niveau de carburant, la température du moteur et les alertes de sécurité.</li>
                <li><strong>Rétroviseurs :</strong> Permettent de voir derrière et sur les côtés du véhicule. Ajustez-les avant de conduire pour une meilleure visibilité.</li>
                <li><strong>Ceinture de sécurité :</strong> Doit toujours être attachée pour votre sécurité et celle de vos passagers.</li>
            </ul>
